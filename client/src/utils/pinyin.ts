function getPinyinInitial(char: string): string {
  if (!char || char.length !== 1) return char
  
  const code = char.charCodeAt(0)
  
  if (code >= 0x4E00 && code <= 0x9FA5) {
    for (const [initial, chars] of Object.entries(PINYIN_MAP)) {
      if (chars.includes(char)) {
        return initial
      }
    }
    return char
  }
  
  if (char >= 'a' && char <= 'z') return char
  if (char >= 'A' && char <= 'Z') return char.toLowerCase()
  
  return char
}

export function getPinyinInitials(text: string): string {
  if (!text) return ''
  return text.split('').map(getPinyinInitial).join('')
}

export function matchSearch(text: string, query: string): boolean {
  if (!text || !query) return false
  
  const normalizedText = text.toLowerCase().trim()
  const normalizedQuery = query.toLowerCase().trim()
  
  if (normalizedText.includes(normalizedQuery)) {
    return true
  }
  
  if (/^[a-z]+$/.test(normalizedQuery)) {
    const pinyinInitials = getPinyinInitials(text)
    if (pinyinInitials.includes(normalizedQuery)) {
      return true
    }
  }
  
  return false
}